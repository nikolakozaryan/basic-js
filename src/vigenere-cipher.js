const alphabet = vigenereCube[0];

class VigenereCipheringMachine {
  constructor(isDirect = true) {
    this.isDirect = isDirect;
  }
  encrypt(message, key) {
    if(!message || !key) throw Error('Incorrect arguments!')

    message = message.toUpperCase()
                     .split('')
                     .map(symbol => alphabet.includes(symbol) ? alphabet.indexOf(symbol) : symbol);

    key = key.toUpperCase()
             .split('')
             .map(symbol => alphabet.includes(symbol) ? alphabet.indexOf(symbol) : symbol);
    
    let correction = 0;
    let encryptedMessage = message.map((letter, index) => {
      if (typeof letter !== 'number') {
        correction++;
        return letter;
      } else return alphabet[(letter + key[(index - correction) % key.length]) % 26]
    });
    
    return !this.isDirect ? encryptedMessage.reverse().join('') : encryptedMessage.join('');
  }
  decrypt(encryptedMessage, key) {
    if(!encryptedMessage || !key) throw Error('Incorrect arguments!')

    encryptedMessage = encryptedMessage.toUpperCase()
                                       .split('')
                                       .map(symbol => alphabet.includes(symbol) ? alphabet.indexOf(symbol) : symbol);

    key = key.toUpperCase()
             .split('')
             .map(symbol => alphabet.includes(symbol) ? alphabet.indexOf(symbol) : symbol);
             
    let correction = 0;
    let message = encryptedMessage.map((letter, index) => {
      if (typeof letter !== 'number') {
        correction++;
        return letter;
      } else return alphabet[(letter + 26 - key[(index - correction) % key.length]) % 26]
    });
    
    return !this.isDirect ? message.reverse().join('') : message.join('');
  }
}

